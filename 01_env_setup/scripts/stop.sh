#!/usr/bin/env bash
# ----------------------------------------------------------------------------
# 컨테이너 및 볼륨 정리 스크립트
# - docker compose down -v : 컨테이너 중지 + 익명 볼륨 삭제
# ----------------------------------------------------------------------------
set -euo pipefail  # 안전 모드 설정

# compose 파일 위치(상위 폴더)로 이동
DIR=$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)
cd "$DIR/.."

# 컨테이너와 볼륨 정리
docker compose down -v

echo "🛑 컨테이너 및 볼륨 정리 완료"
